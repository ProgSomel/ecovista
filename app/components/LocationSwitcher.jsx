"use client";

import { getLocationLatLongList } from "@/lib/location-info";
import Image from "next/image";
import Link from "next/link";
import React, { useEffect, useState } from "react";

export default function LocationSwitcher() {
  const [showLocationList, setShowLocationList] = useState(false);
  const [locations, setLocations] = useState([]);

  useEffect(() => {
    async function getLocationList() {
      const locationList = await getLocationLatLongList();
      setLocations(locationList);
    }
    getLocationList();
  }, []);

  return (
    <div className="relative">
      <button
        className="cursor-pointer"
        onClick={() => setShowLocationList(!showLocationList)}
      >
        <Image
          className="size-9"
          src="link.svg"
          alt="link icon"
          width={10}
          height={10}
        />
      </button>
      {showLocationList && (
        <div className="absolute left-0 top-12 z-999 w-full min-w-70 rounded-md bg-white p-4 shadow max-md:-translate-x-1/2">
          <ul
            role="list"
            className="divide-y divide-gray-100 *:py-2 [&>li]:cursor-pointer"
          >
            {locations?.map((info) => (
              <li key={info.location}>
                <Link
                  href={`/${info.location}?latitude=${info.latitude}&longitude=${info.longitude}`}
                >
                  {info.location}
                </Link>
              </li>
            ))}
          </ul>
        </div>
      )}
    </div>
  );
}
